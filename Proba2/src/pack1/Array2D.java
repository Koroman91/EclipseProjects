package pack1;

public class Array2D {
	
	public static void main(String[] args)
	{
		int p[][] = { {1,5,7,8}, {2,3,9,8}, {7,8,3,5}};	
		
	    for(int i = 0; i<3; i++)
	    {
	    	for (int j=0; j<4; j++)
	    	{
	    		System.out.print(p[i][j] + " ");
	    	}
	    	System.out.println("");
	    }
	    
	    int k[][] = new int[3][];
	    
	    k[0] = new int[4];
	    k[1] = new int[3];
	    k[2] = new int[2];
	    
	    
	    
	   
	    
	    
		
	}

}
